/**
 * Get unique cities from mock data
 * @returns {string[]} Array of unique city names
 */
export const getUniqueCities = () => {
  const cities = mockHotels.map((hotel) => hotel.location.city);
  return [...new Set(cities)].sort();
};

/**
 * Get price range from mock data
 * @returns {{ min: number, max: number }} Min and max prices
 */
export const getPriceRange = () => {
  const prices = mockHotels.map((hotel) => hotel.pricePerNight);
  return {
    min: Math.min(...prices),
    max: Math.max(...prices),
  };
};

/**
 * Get all unique amenities from mock data
 * @returns {string[]} Array of unique amenity keys
 */
export const getAllAmenities = () => {
  const amenities = mockHotels.flatMap((hotel) => hotel.amenities);
  return [...new Set(amenities)].sort();
};

